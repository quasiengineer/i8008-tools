const runSingleTest = (romDump, { a, b }) => {
  const system = new Emulator({ memoryDump: romDump });

  const { registers } = system;
  registers.B = a & 0xFF;
  registers.C = (a >> 8) & 0xFF;
  registers.D = a >> 16;

  registers.E = b & 0xFF;
  registers.H = b >> 8;

  while (!system.isFinished) {
    system.instruction();
  }

  return {
    result: {
      reminder: registers.B + (registers.C << 8),
      quotient: registers.L,
    },
    elapsedStates: system.tStates,
  };
};

