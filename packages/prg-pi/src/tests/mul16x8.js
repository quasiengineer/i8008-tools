const runSingleTest = (romDump, { a, b }) => {
  const system = new Emulator({ memoryDump: romDump });

  const { registers } = system;
  registers.A = a & 0xFF;
  registers.B = a >> 8;
  registers.C = b;

  while (!system.isFinished) {
    system.instruction();
  }

  return {
    result: (registers.E << 16) + (registers.H << 8) + registers.L,
    elapsedStates: system.tStates,
  };
};

